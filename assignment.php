//subtraction
$first_number -= $second_number; //120
echo "<br>";
echo $first_number;

//multiplication
$first_number *= $second_number;
echo "<br>";
echo $first_number; //2400

//division
$first_number /= $second_number; //120
echo "<br>";
echo $first_number;

//modulo
$first_number %= $second_number; //120
echo "<br>";
